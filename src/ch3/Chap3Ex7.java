package ch3;

// Demonstration for switch case
public class Chap3Ex7 {
    public static void main(String[] args) {
        switch(2){
            case 1:
                System.out.println("One");
                break;
            case 2:
                System.out.println("Two");
                break;
            default:
                System.out.println("Not recognised");
                break;
        }

    }
}
